public class Assignmentoperator {
    public static void main(String[] args) {
        int a = 20;

        System.out.println("Initial = " + a);

        a += 5;
        System.out.println("+= " + a);

        a -= 3;
        System.out.println("-= " + a);

        a *= 2;
        System.out.println("*= " + a);

        a /= 2;
        System.out.println("/= " + a);

        a %= 4;
        System.out.println("%= " + a);
    }
}